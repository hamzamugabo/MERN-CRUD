const mongoose = require('mongoose');
const Schema = mongoose.Schema;

let Todo2 = new Schema({
    username: {
        type: String,
        required: true,
        minlength: 5,
        maxlength: 50
    },
    email: {
        type: String,
        required: true,
        minlength: 5,
        maxlength: 255,
        unique: true
    },
    password: {
        type: String,
        required: true,
        minlength: 5,
        maxlength: 1024
    }
});

module.exports = mongoose.model('Todo2', Todo2);